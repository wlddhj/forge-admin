-- =============================================================================
-- 租户管理员角色初始化
-- =============================================================================
-- 创建租户时，SysTenantServiceImpl.addTenant 会自动创建一个属于此角色的管理员账号。
-- 角色与 SUPER_ADMIN 的区别：
--   - SUPER_ADMIN (account_type=2)：平台超管，跨租户
--   - TENANT_ADMIN：本租户内的管理员，account_type=0
--
-- 菜单策略：当前套餐菜单接入未完成，sys_role_menu 直接决定可见菜单。
--   临时给 TENANT_ADMIN 分配所有非租户管理相关菜单（避免误给租户管理权限）。
--   待套餐接入后，应按租户套餐范围限定菜单候选。
-- =============================================================================

-- ---------------------------------------------------------------------------
-- 1. 插入 TENANT_ADMIN 角色
-- ---------------------------------------------------------------------------
INSERT IGNORE INTO sys_role (id, role_name, role_code, description, is_fixed, status, data_scope, sort_order, create_time, update_time, deleted)
VALUES (3, '租户管理员', 'TENANT_ADMIN', '本租户内全权管理（用户、部门、角色等）', 1, 1, '1', 50, NOW(), NOW(), 0);

-- ---------------------------------------------------------------------------
-- 2. 分配菜单：业务菜单 + 用户/部门/角色/字典/岗位等基础管理菜单
--    排除：菜单管理（4）、OAuth2 客户端（70）、App 用户（246）、租户管理（2400-2414）
-- ---------------------------------------------------------------------------
INSERT IGNORE INTO sys_role_menu (role_id, menu_id)
SELECT 3, id FROM sys_menu
WHERE id NOT IN (
    -- 排除平台专属管理菜单
    4,   -- 菜单管理（仅平台超管）
    70,  -- OAuth2 客户端
    246, -- App 用户
    -- 排除租户管理
    2400, 2401, 2402, 2403, 2404, 2405, 2410, 2411, 2412, 2413, 2414
)
AND deleted = 0;

-- ---------------------------------------------------------------------------
-- 3. 关联按钮权限（同上，排除租户管理按钮）
-- ---------------------------------------------------------------------------
-- 上面 SQL 已按 menu_id 过滤所有租户/平台专属菜单，自动包含各业务模块的按钮权限
